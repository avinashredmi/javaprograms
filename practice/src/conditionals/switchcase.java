package conditionals;

public class switchcase {
	public static void main(String[] args) {
		int i=9;
		switch (i) {
		case 1:
		System.out.println("1");
		case 6:
			System.out.println("6");
			break;
		case 7:
			System.out.println("7");
		
			
			break;

		default:
			System.out.println("nothing");
			break;
		}
	}

}
